"""
    Crie uma lista e utilize um loop for para percorrer todos os elementos da lista.
"""

def percorrer_lista_com_for():
    lista = ['item1', 'item2', 'item3', 'item4', 'item5', 'item6', 'item7']
    
    for item in lista:
        print(item)
        
if __name__ == '__main__':
    percorrer_lista_com_for()